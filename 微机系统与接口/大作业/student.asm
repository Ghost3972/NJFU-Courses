DATA SEGMENT
    STUNUM DB 10  ;
    GRADE DB 80,100,78,56,79,90,96,82,88,75
    A100 DB 0
    B90 DB 0
    C80 DB 0
    D70 DB 0
    E60 DB 0
    F50 DB 0
    DATA ENDS
STACK SEGMENT STACK
    DB 200 DUP(0)
    STACK ENDS
CODE SEGMENT
    ASSUME CS:CODE,SS:STACK,DS:DATA
    START: MOV AX,DATA
    MOV DS,AX
    MOV A100,0
    MOV B90,0
    MOV C80,0
    MOV D70,0
    MOV E60,0
    MOV F50,0
    LEA BX,STUNUM
    MOV CX,[BX] 
    AND CX,00FFH
    LEA BX,GRADE
    COMPARE: MOV AX, [BX]
    AND AX,00FFH
    CMP AX,60
    JB FIVE
    CMP AX,70
    JB SIX
    CMP AX,80
    JB SEVEN
    CMP AX,90
    JB EIGHT
    CMP AX,100
    JB NINE
    INC A100
    JMP SHORT NEXT
    FIVE:     INC F50
    JMP SHORT NEXT
    SIX:      INC E60
    JMP SHORT NEXT
    SEVEN:    INC D70
    JMP SHORT NEXT
    EIGHT:    INC C80
    JMP SHORT NEXT
    NINE:     INC B90
    NEXT:     INC BX
    LOOP COMPARE
    MOV AH,4CH
    INT 21H
    CODE ENDS
END START
